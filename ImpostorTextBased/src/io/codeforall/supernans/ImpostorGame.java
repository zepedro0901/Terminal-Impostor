    public synchronized void getAPrompt() throws IOException {
        int promptIndex = (int) Math.floor(Math.random() * promptList.size());
        for (ServerWorker serverWorker : server.serverWorkerList) {
            if (!serverWorker.impostor) {
                serverWorker.bufferedWriter.write("The game has started!\nThe game prompt is " + promptList.get(promptIndex));
                serverWorker.bufferedWriter.newLine();
                serverWorker.bufferedWriter.flush();

            }

        }
        server.promptGiven = true;

    }

